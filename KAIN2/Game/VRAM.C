int VRAM_InsertFreeBlock(struct _BlockVramEntry* block)
{
	struct _BlockVramEntry* next;
	struct _BlockVramEntry* prev;

	prev = NULL;
	if (block == NULL)
	{
		return 0;
	}

	next = openVramBlocks;
	while (next != NULL)
	{
		if (next->area >= block->area)
		{
			break;
		}

		prev = next;
		next = prev->next;
	}


	if (prev == NULL)
	{
		block->next = openVramBlocks;
		openVramBlocks = block;
	}
	else
	{
		block->next = next;
		prev->next = block;
	}

	VRAM_GarbageCollect();

	return 1;
}

